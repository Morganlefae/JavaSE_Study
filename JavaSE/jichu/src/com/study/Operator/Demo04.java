package com.study.Operator;

public class Demo04 {
    public static void main(String[] args) {
        int a = 3;
        System.out.println(a);
        int b = a++;
        int c = ++a;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        //幂运算
        double pow = Math.pow(2,3);
        System.out.println(pow);

    }
}
